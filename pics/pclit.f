C RANDOMLY DISTRIBUTE PARTICLES IN RECTANGULAR COORDINATES
C NEED G05DAF FROM NAG
C COLLECT OUTPUT TO FILE AND VIM with %s/^/1^M'#183'^MS^M;1,2m$
C FOR INPUT TO ANNOT
	DOUBLE PRECISION QXRAN,QYRAN,G05DAF
	DOUBLE PRECISION X0,Y0,X1,Y1
C BOTTOM LEFT CORNER (TO GREEN)
        X0=0.52D0
        Y0=0.19D0
C (TO BLUE)
        X0=0.76D0
        Y0=0.10D0
C (TO ELTS)
        X0=0.10D0
        Y0=0.10D0
C (TO ELTS_BLUE)
        X0=0.10D0
        Y0=0.50D0
C TOP RIGHT CORNER
        X1=0.72D0
        Y1=0.30D0
C (TO BLUE)
        X1=0.96D0
        Y1=0.30D0
C (TO ELTS)
        X1=0.90D0
        Y1=0.90D0
C (TO ELTS_BLUE)
        X1=0.40D0
        Y1=0.90D0
C NUMBER OF POINTS O/P
        NIMAX=50
        I=0
 	DO 30 J=1,3*NIMAX
 	QXRAN=G05DAF(X0,X1)
 	QYRAN=G05DAF(Y0,Y1)
 	ZXRAN=QXRAN
 	ZYRAN=QYRAN
        IF ( ZXRAN > 1.02*X0 .AND. ZXRAN < 0.98*X1 ) THEN
        IF ( ZYRAN > 1.02*Y0 .AND. ZYRAN < 0.98*Y1 ) THEN
	WRITE(6,*) ZXRAN,ZYRAN
        I=I+1
        IF (I>NIMAX) GO TO 999
        END IF
        END IF
  30    CONTINUE
 999    CONTINUE
	STOP
	END
